package Dispositivo;

public class Dispositivo{

	private String nombre;
	private int consumoPorHora;
	private bool estado;
	
	public Dispositivo(String nombre, int consumoPorHora, bool estado) {
		super();
		this.nombre = nombre;
		this.consumoPorHora = consumoPorHora;
		this.estado = estado;
	}
	
}

