package Categoria;

public class Categoria{

	private int consumoMensual;
	private String cargo;
	private String unidad;
	private String tipo;
	private String concepto;
	
	public Categoria(int consumoMensual, String cargo, String unidad, String tipo, String concepto) {
		super();
		this.consumoMensual = consumoMensual;
		this.cargo = cargo;
		this.unidad = unidad;
		this.tipo = tipo;
		this.concepto = concepto;
	}

}